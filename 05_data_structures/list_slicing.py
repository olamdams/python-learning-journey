fruits = ["apple", "banana", "orange", "kiwi", "mango"]
print(fruits[1:4])  # prints items from index 1 to 3, which are "banana", "orange", and "kiwi"
print(fruits[:3])   # prints items from the beginning to index 2, which are "apple", "banana", and "orange"
print(fruits[2:5])  # prints items from index 2 to 4, which are "orange", "kiwi", and "mango"