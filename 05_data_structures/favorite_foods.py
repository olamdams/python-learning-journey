favourite_foods = ["pizza", "ice cream", "tacos", "sushi", "pasta", "burgers", "salad", "chocolate", "steak", "fries"]

print(favourite_foods)
print(favourite_foods[0])  # prints the first item in the list
print(favourite_foods[-1])  # prints the last item in the list
print(favourite_foods[2:5])  # prints items from index 2 to 4
print(favourite_foods[:3])  # prints the first three items
print(favourite_foods[3:7])  # prints items from index 3 to 6
